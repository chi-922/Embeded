/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MPU Configuration--------------------------------------------------------*/
	MPU_Config();

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_TIM3_Init();
	MX_TIM12_Init();
	/* USER CODE BEGIN 2 */
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_3);  // PBO 해당하는 채널
	HAL_TIM_PWM_Start(&htim12, TIM_CHANNEL_1);  // PB14 해당하는 채널
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		// 2초 동안 듀티 0%→100%로 변화
		uint32_t period1 = __HAL_TIM_GET_AUTORELOAD(&htim3); // Counter Period 값
		uint32_t period2 = __HAL_TIM_GET_AUTORELOAD(&htim12); // Counter Period 값
		uint32_t steps = 100;  // 100단계로 밝기 변경
		uint32_t delayPerStep = 5000 / steps; // 5000ms / 100단계 = 50ms마다 변경

		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
		for (uint32_t i = 0; i <= steps; i++)
		{
			uint32_t duty2 = (period2 * i) / steps;               // 0→100%
			uint32_t duty1 = (period1 * (steps - i)) / steps;     // 100→0%
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, duty1);  // PB0 LED
			__HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, duty2);  // PB14 LED
			HAL_Delay(delayPerStep);
		}

		// LED 꺼진 상태에서 다시 반복하거나 원하는 동작 추가 가능
		HAL_Delay(1000); // 잠시 유지
		//__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 0); // 꺼짐
		//__HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_1, 0); // 꺼짐
		HAL_Delay(500);
	}
	/* USER CODE END 3 */
}

